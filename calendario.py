numero = int(input("digite um numero de 1 a 7: "))

if numero == 1:
    print('é domingo!')
elif numero == 2:
    print('é segunda-feira!')
elif numero == 3:
    print('é terça-feira!')
elif numero == 4:
    print('é quarta-feira!')
elif numero == 5:
    print('é quinta-feira!')
elif numero == 6:
    print('é sexta!')
elif numero == 7:
    print('é sabado!')
else:
    print('Digitou um numero invalido')
    
